var (
	rrCache = sync.Map{}
)

func NewRR(s string) dns.RR {
	if rr, ok := rrCache.Load(s); ok {
		return rr.(dns.RR)
	}

	r, _ := dns.NewRR(s)
	rrCache.Store(s, r)
	return r
}

